using UnityEngine;
using System.Collections;

public class Hippo
{
    public Hippo(Tag tag, string identity)
    {
        this.Tag = tag;
		this.Identity = identity;
    }

    public void EatBall()
    {
        BallsEaten++;
    }

    public Tag Tag { get; private set; }
	public string Identity { get; private set; }
    public int BallsEaten { get; private set; }
}
